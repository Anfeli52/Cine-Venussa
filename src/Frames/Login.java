    private void lblLogInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblLogInMouseClicked
        correo = txtUsername.getText();
        password = String.valueOf(txtPassword.getPassword());
        boolean init = dbc.LogIn(correo, password);
        if(init){
            setVisible(false);
            MoviesSelector movieSelector = new MoviesSelector();
            movieSelector.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Correo o ContraseÃ±a Incorrectos");
        }
    }//GEN-LAST:event_lblLogInMouseClicked

    private void txtUsernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtUsernameFocusLost
        if(String.valueOf(txtPassword.getPassword()).equals("**********")){
            txtPassword.setText("");
            txtPassword.setForeground(Color.black);
        }
        if(txtUsername.getText().isEmpty()){
            txtUsername.setText("Write your email");
            txtUsername.setForeground(Color.gray); 
        }
    }//GEN-LAST:event_txtUsernameFocusLost

    private void txtPasswordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPasswordFocusLost
        if(txtUsername.getText().equals("Write your email")){
            txtUsername.setText("");
            txtUsername.setForeground(Color.black);
        }
        if(String.valueOf(txtPassword.getPassword()).isEmpty()){
            txtPassword.setText("**********");
            txtPassword.setForeground(Color.gray); 
        }
    }//GEN-LAST:event_txtPasswordFocusLost

